panjang = int(input("Masukkan Nilai Panjang : ")) # Di soal Panjang 1o cm
lebar = int(input("Masukkan Nilai Lebar : ")) # Di soal Lebar 6 cm
Tinggi = int(input("Masukkan Nilai Tinggi : ")) # Di soal Tinggi 4 cm
volume = panjang * lebar * Tinggi # Rumus Volume Adalaah p*l*t
luas = 2*(panjang*lebar + panjang*Tinggi + lebar*Tinggi) # rumus Luas Adalah 2x(p*l*t)
print("Volume Dari Balok Tersebut Adalah : ",volume)
print("Luas Dari Balok Tersebut Adalah : ",luas)